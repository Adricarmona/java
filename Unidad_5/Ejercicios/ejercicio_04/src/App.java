public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Numeros del 320 al 160 de 20 en 20 para atras");
        System.out.println("---------------------------------------------");
        for (int i = 320; i > 159; i = i - 20) {
            System.out.print(i+" ");
        }
    }
}
